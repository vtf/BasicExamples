package com.example.graph;

public class Edge {

	private NodeGraph origin;
	
	private NodeGraph destiny;
	
	private int distance;


	public NodeGraph getOrigin() {
		return origin;
	}

	public void setOrigin(NodeGraph origin) {
		this.origin = origin;
	}

	public NodeGraph getDestiny() {
		return destiny;
	}

	public void setDestiny(NodeGraph destiny) {
		this.destiny = destiny;
	}

	public int getDistance() {
		return distance;
	}

	public void setDistance(int distance) {
		this.distance = distance;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((destiny == null) ? 0 : destiny.hashCode());
		result = prime * result + distance;
		result = prime * result + ((origin == null) ? 0 : origin.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Edge other = (Edge) obj;
		if (destiny == null) {
			if (other.destiny != null)
				return false;
		} else if (!destiny.equals(other.destiny))
			return false;
		if (distance != other.distance)
			return false;
		if (origin == null) {
			if (other.origin != null)
				return false;
		} else if (!origin.equals(other.origin))
			return false;
		return true;
	}
	
	
	
}
